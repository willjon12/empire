    private void btokActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btokActionPerformed
        if (flagt == 1) {
            if (tbtos.getRowCount() != 0) {
                int ul = tbtos.getRowCount();
                for (int l = 0; l < ul; l++) {
                    Titulo t = new Titulo();
                    t.setDoc(Integer.parseInt(tbtos.getValueAt(l, 0).toString()));
                    t.setTipo(tbtos.getValueAt(l, 1).toString());
                    t.setQtdparcela(tbtos.getValueAt(l, 2).toString());
                    t.setIdclifor(Integer.parseInt(tbtos.getValueAt(l, 3).toString()));
                    t.setClifor(tbtos.getValueAt(l, 4).toString());
                    t.setFormpag(tbtos.getValueAt(l, 5).toString());
                    t.setVlrparcela(Double.parseDouble(tbtos.getValueAt(l, 6).toString()));
                    t.setVlrtotal(Double.parseDouble(tbtos.getValueAt(l, 7).toString()));
                    t.setDte(Util.convertedataUS(tbtos.getValueAt(l, 8).toString()));
                    t.setDtv(Util.convertedataUS(tbtos.getValueAt(l, 9).toString()));
                    t.setDescos(tbtos.getValueAt(l, 10).toString());
                    if (tbtos.getValueAt(l, 5).toString().equals("Dinheiro")) {
                        t.setBaixado(true);
                        t.setVlrbaixa(Double.parseDouble(tbtos.getValueAt(l, 6).toString()));
                        t.setDtb(Util.convertedataUS(tbtos.getValueAt(l, 8).toString()));
                    } else {
                        t.setBaixado(false);
                        t.setVlrbaixa(0.0);
                        t.setDtb("");
                    }
                    t.setVlrpago(0.0);
                    
                    titos.add(t);
                    this.dispose();
                }
            } else {
                JOptionPane.showMessageDialog(tfvencimento, "Gerar pelo menos uma parcela para continuar");
            }
        }
        if (flagt == 2) {
            this.dispose();
        }

    }//GEN-LAST:event_btokActionPerformed

    private void btgerartActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btgerartActionPerformed
        DefaultTableModel t = (DefaultTableModel) tbtos.getModel();
        t.setNumRows(0);

        String docos = tfdocos.getText();
        String idclifor = tfcodp.getText();
        String clifor = tfpessoa.getText();
        int parc = Integer.parseInt(tfparcela.getValue().toString());
        String tipo = cbtipo.getSelectedItem().toString();
        String formpag = cbformpag.getSelectedItem().toString();
        String descos = tfdescobs.getText();
        String dte = Util.datatoStringBR(tfemissao.getDate());
        int pvenc = Integer.parseInt(tfvencimento.getValue().toString());
        Double vlrparc = Double.parseDouble(tfvlrparcela.getText().replace(",", "."));
        Double vlrtot = Double.parseDouble(tfvlrtotal.getText().replace(",", "."));
        Double vlrentrada = Double.parseDouble(tfentrada.getText().replace(",", "."));
        int l2 = 1;

        if (vlrentrada != 0) {
            if (Objects.equals(vlrentrada, vlrtot)) {
                parc = 0;
                tfparcela.setValue(1);
            }

            parc += 1;
            t.addRow(new Object[]{docos, tipo, 1 + "/" + parc, idclifor, clifor, "Dinheiro", vlrentrada, vlrtot, dte,
                dte, "Ref. entrada - " + descos});
            l2 = 2;

        }
        if (vlrentrada < vlrtot) {
            for (int l = l2; l <= parc; l++) {
                try {
                    Calendar calend = Calendar.getInstance();
                    calend.setTime(tfemissao.getDate());
                    if (vlrentrada != 0) {
                        int l3 = l - 1;
                        calend.set(Calendar.DAY_OF_MONTH, calend.get(Calendar.DAY_OF_MONTH) + pvenc * l3);
                    } else {
                        calend.set(Calendar.DAY_OF_MONTH, calend.get(Calendar.DAY_OF_MONTH) + pvenc * l);
                    }

                    t.addRow(new Object[]{docos, tipo, l + "/" + parc, idclifor, clifor, formpag, vlrparc, vlrtot, dte,
                        (dtbr.format(calend.getTime())), descos});

                } catch (Exception ex) {
                    JOptionPane.showMessageDialog(tfvencimento, "Erro ao Gerar Parcelas");
                    printStackTrace(ex);
                }

            }
        }
    }//GEN-LAST:event_btgerartActionPerformed

