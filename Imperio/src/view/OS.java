    private void btpesqosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btpesqosActionPerformed
        limpaos();
        desativaOS();
        Buscaos buscaos;
        buscaos = new Buscaos(this, true, tfpesq.getText());
        buscaos.setVisible(true);
        try {

            tfcodos.setText(String.valueOf(buscaos.o.getCod()));
            tfdta.setDate(Util.dataBR(buscaos.o.getDta()));
            tfdtf.setDate(Util.dataBR(buscaos.o.getDtf()));
            tfdtg.setDate(Util.dataBR(buscaos.o.getDtg()));
            tfusuario.setText(buscaos.o.getUseros());
            tfdescricao.setText(buscaos.o.getDescricao());
            tfcod.setText(String.valueOf(buscaos.o.getCodclifor()));
            tfcliente.setText(buscaos.o.getClifor());
            tfdesconto.setText(String.valueOf(buscaos.o.getDesconto()));
            cbstatus.setSelectedItem(buscaos.o.getStatus());
            tfobs.setText(buscaos.o.getObs());

            int ul = buscaos.itens.size();

            for (int l = 0; l < ul; l++) {
                DefaultTableModel t = (DefaultTableModel) tblItens.getModel();
                Item i = buscaos.itens.get(l);

                t.addRow(new Object[]{i.getDescricao(), i.getQtd(), i.getUN(),
                    i.getVenda(), i.getTotalitem(), i.getId()});

            }

            ativaOS();
            ut();
            flag = 2;
            tfpesq.setText("");
            cbstatus.setEnabled(true);
            if (buscaos.o.getStatus().equals("Fechado")) {
                btsalvar.setEnabled(false);
                btnovoitem.setEnabled(false);
                bteditaritem.setEnabled(false);
                btexcluiritem.setEnabled(false);
                btpesqp.setEnabled(false);
                cbstatus.setEnabled(false);
                tfdtg.setEnabled(false);
                tfdtgd.setEnabled(false);
            }

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(rootPane, "Nenhuma O.S Selecionada");
            tfpesq.setText("");
            tfcodos.setText("");
        }

    }//GEN-LAST:event_btpesqosActionPerformed

