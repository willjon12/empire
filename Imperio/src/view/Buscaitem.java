    public void populaprod() {

        DefaultTableModel t = (DefaultTableModel) tblprod.getModel();
        t.setNumRows(0);
        formprod();

        int tamanho;
        List<Item> lista = produtoControl.listar2(tfpesq.getText());

        tamanho = lista.size();
        for (int i = 0; i < tamanho; i++) {
            Item prod = lista.get(i);

            t.addRow(new Object[]{0});

            t.setValueAt(prod.getCodproduto(), i, 0);
            t.setValueAt(prod.getDescricao(), i, 1);
            t.setValueAt(Util.convertedataBR(prod.getDatacad()), i, 2);
            t.setValueAt(prod.getCusto(), i, 3);
            t.setValueAt(prod.getVenda(), i, 4);
            t.setValueAt(prod.getLucro(), i, 5);
            t.setValueAt(prod.getUN(), i, 6);

        }

    }

