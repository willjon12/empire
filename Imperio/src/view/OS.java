    private void bteditaritemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bteditaritemActionPerformed
        if (tos == null) {

        } else {
            tos.titos.clear();
        }

        if (tblItens.getSelectedRow() >= 0) {
            int l = tblItens.getSelectedRow();
            ositem = new OSitem(null, true, true);

            DefaultTableModel t = (DefaultTableModel) tblItens.getModel();
            Item i = new Item();
            i.setDescricao(t.getValueAt(l, 0).toString());
            i.setQtd(Double.parseDouble(t.getValueAt(l, 1).toString()));
            i.setUN(t.getValueAt(l, 2).toString());
            i.setVenda(Double.parseDouble(t.getValueAt(l, 3).toString()));
            i.setTotalitem(Double.parseDouble(t.getValueAt(l, 4).toString()));

            ositem.rv(i);
            ositem.setVisible(true);
            if (ositem.i.isFechado()) {

            } else {
                t.setValueAt(ositem.i.getDescricao(), l, 0);
                t.setValueAt(ositem.i.getQtd(), l, 1);
                t.setValueAt(ositem.i.getUN(), l, 2);
                t.setValueAt(ositem.i.getVenda(), l, 3);
                t.setValueAt(ositem.i.getTotalitem(), l, 4);
                ut();
            }

        } else {
            JOptionPane.showMessageDialog(rootPane, "Selecione um Item para editar");
        }

    }//GEN-LAST:event_bteditaritemActionPerformed

