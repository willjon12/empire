    private void calcvlr() {
        Double vlrtot = Double.parseDouble(tfvlrtotal.getText().replace(",", "."));
        int parc = Integer.parseInt(tfparcela.getValue().toString());
        Double entrada = Double.parseDouble(tfentrada.getText().replace(",", "."));
        Double vfinal = vlrtot - entrada;
        Double calc = vfinal / parc;
        tfvlrparcela.setText(String.valueOf(dc.format(calc)));
    }
    
