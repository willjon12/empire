    private void btsalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btsalvarActionPerformed
        if (tfdescricao.getText().equals("") | tfcusto.getText().equals("") | tfmargem.getText().equals("")
                | tfvenda.getText().equals("") | tfun.getText().equals("")) {
            JOptionPane.showMessageDialog(rootPane, "Preencha os campos obrigatórios");

        } else {

            if (flag == 1) {
                if (prodcontrol.salvar(tfdescricao.getText(), Util.convertedataUS(tfdatacad.getText()), Double.parseDouble(tfcusto.getText().replace(",", ".")),
                        Double.parseDouble(tfvenda.getText().replace(",", ".")), Double.parseDouble(tfmargem.getText().replace(",", ".")), tfun.getText())) {
                    JOptionPane.showMessageDialog(rootPane, "Item cadastrado com sucesso");

                } else {
                    JOptionPane.showMessageDialog(rootPane, "Item não cadastrado");
                }

            }
            if (flag == 2) {
                if (prodcontrol.alterar(Integer.parseInt(tfcod.getText()), tfdescricao.getText(), Util.convertedataUS(tfdatacad.getText()), Double.parseDouble(tfcusto.getText().replace(",", ".")),
                        Double.parseDouble(tfvenda.getText().replace(",", ".")), Double.parseDouble(tfmargem.getText().replace(",", ".")), tfun.getText())) {
                    JOptionPane.showMessageDialog(rootPane, "Item Alterado com sucesso");
                } else {
                    JOptionPane.showMessageDialog(rootPane, "Item não alterado");
                }
            }
            limpatela();
            desativatela();
            btnovo.setEnabled(true);
        }


    }//GEN-LAST:event_btsalvarActionPerformed

    private void btcancelarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btcancelarActionPerformed
        int reply = JOptionPane.showConfirmDialog(null, "Deseja realmente Cancelar (As Alterações realizadas serão perdidas se não clicar em salvar) ", "Tem certeza", JOptionPane.YES_NO_OPTION);
        if (reply == JOptionPane.YES_OPTION) {
            desativatela();
            limpatela();
            btnovo.setEnabled(true);
        } else {
            JOptionPane.showMessageDialog(null, "Clique em salvar para guardar os dados");
        }


    }//GEN-LAST:event_btcancelarActionPerformed

