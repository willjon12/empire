    public void populaosg() {

        DefaultTableModel t = (DefaultTableModel) tbgos.getModel();
        t.setNumRows(0);

        tbgos.getColumnModel().getColumn(0).setPreferredWidth(55);
        tbgos.getColumnModel().getColumn(1).setPreferredWidth(157);
        tbgos.getColumnModel().getColumn(2).setPreferredWidth(140);
        tbgos.getColumnModel().getColumn(3).setPreferredWidth(80);

        int tamanho;
        ConfigDAO cd = new ConfigDAO();
        Util.c = cd.listarconfig().get(0);

        OsDAO od = new OsDAO();

        int co = Util.c.getDav();

        int dias = (co);
        lbcdias.setText(dias + " Dias");
        Calendar calend = Calendar.getInstance();
        Date data = new Date();
        calend.setTime(data);
        String dtgi = Util.datatoStringUS(data);
        calend.set(Calendar.DAY_OF_MONTH, calend.get(Calendar.DAY_OF_MONTH) + dias);

        String dtgf = Util.datatoStringUS(calend.getTime());

        List<Os> lista = od.listarosg(dtgi, dtgf);

        tamanho = lista.size();
        for (int i = 0; i < tamanho; i++) {
            Os os = lista.get(i);

            t.addRow(new Object[]{0});

            t.setValueAt(os.getCod(), i, 0);
            t.setValueAt(os.getDescricao(), i, 1);
            t.setValueAt(Util.difemdias(Util.DateToDTM(data), Util.StringToDTM(os.getDtg())), i, 2);
            if (os.getDtg() == null) {
                t.setValueAt("", i, 3);
            } else {
                t.setValueAt(Util.convertedataBR(os.getDtg()), i, 3);
            }

        }
    }

    public void populaospesqp() {

        DefaultTableModel t = (DefaultTableModel) tbosp.getModel();
        t.setNumRows(0);
        tbosp.getColumnModel().getColumn(0).setPreferredWidth(55);
        tbosp.getColumnModel().getColumn(1).setPreferredWidth(150);
        tbosp.getColumnModel().getColumn(2).setPreferredWidth(82);
        tbosp.getColumnModel().getColumn(3).setPreferredWidth(150);
        tbosp.getColumnModel().getColumn(4).setPreferredWidth(80);
        tbosp.getColumnModel().getColumn(5).setPreferredWidth(70);

        String status = cbstatus.getSelectedItem().toString();

        String dtfi = Util.datatoStringUS(tfdtfi.getDate());
        String dtff = Util.datatoStringUS(tfdtff.getDate());

        if (cbstatus.getSelectedItem().equals("Andamento")) {
            dtfi = "";
            dtff = "";
        }

        int tamanho;
        List<Os> lista = oscontrol.listaospesq("",
                0, status,
                "1950-01-01",
                "2050-12-30",
                dtfi,
                dtff,
                0);

        if (lista.isEmpty()) {
            popdata();
        }

        tamanho = lista.size();
        for (int i = 0; i < tamanho; i++) {
            Os os = lista.get(i);

            t.addRow(new Object[]{0});

            t.setValueAt(os.getCod(), i, 0);
            t.setValueAt(os.getDescricao(), i, 1);
            t.setValueAt(Util.convertedataBR(os.getDta()), i, 2);
            t.setValueAt(os.getClifor(), i, 3);
            t.setValueAt(os.getTotal(), i, 4);
            t.setValueAt(os.getStatus(), i, 5);
        }

        if (!lista.isEmpty()) {
            int ul = tbosp.getRowCount();
            Double ttotal = 0.0;
            for (int l = 0; l < ul; l++) {

                ttotal += Double.parseDouble(tbosp.getValueAt(l, 4).toString());
            }
            DecimalFormat dc = new DecimalFormat("#0.00");

            tftotalos.setText(dc.format(ttotal));

        }

    }

