    private void btokActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btokActionPerformed

        int posicao = tblprod.getSelectedRow();
        if (posicao >= 0) {

            p.setCodproduto(Integer.parseInt(tblprod.getValueAt(posicao, 0).toString()));
            p.setDescricao(tblprod.getValueAt(posicao, 1).toString());
            p.setDatacad(tblprod.getValueAt(posicao, 2).toString());
            p.setCusto(Double.parseDouble(tblprod.getValueAt(posicao, 3).toString()));
            p.setVenda(Double.parseDouble(tblprod.getValueAt(posicao, 4).toString()));
            p.setLucro(Double.parseDouble(tblprod.getValueAt(posicao, 5).toString()));
            p.setUN(tblprod.getValueAt(posicao, 6).toString());

            this.dispose();

        } else {
            JOptionPane.showMessageDialog(rootPane, "Selecione um Registro");
        }
    }//GEN-LAST:event_btokActionPerformed

