    public void populaos() {
        DefaultTableModel t = (DefaultTableModel) tbos.getModel();
        t.setNumRows(0);

        int tamanho;
        List<Os> lista = oscontrol.listar();

        tamanho = lista.size();
        for (int i = 0; i < tamanho; i++) {
            Os os = lista.get(i);

            t.addRow(new Object[]{0});

            t.setValueAt(os.getCod(), i, 0);
            t.setValueAt(os.getDescricao(), i, 1);
            t.setValueAt(Util.convertedataBR(os.getDta()), i, 2);
            if (os.getDtf() == null) {
                os.setDtf("");
                t.setValueAt(Util.convertedataBR(os.getDtf()), i, 3);
            }

            t.setValueAt(os.getStatus(), i, 4);
            t.setValueAt(os.getCodclifor(), i, 5);
            t.setValueAt(os.getClifor(), i, 6);
            t.setValueAt(os.getUseros(), i, 7);
            t.setValueAt(os.getObs(), i, 8);
            t.setValueAt(os.getTotal(), i, 9);
            t.setValueAt(os.getDesconto(), i, 10);
            t.setValueAt(os.getSubtotal(), i, 11);

        }
    }

    public void populaios(int cod) {

        DefaultTableModel t = (DefaultTableModel) tbios.getModel();
        t.setNumRows(0);
        formios();

        int tamanho;
        List<Item> lista = oscontrol.listarios(cod);

        tamanho = lista.size();
        for (int i = 0; i < tamanho; i++) {
            Item prod = lista.get(i);

            t.addRow(new Object[]{0});

            t.setValueAt(prod.getDescricao(), i, 0);
            t.setValueAt(prod.getUN(), i, 1);
            t.setValueAt(prod.getQtd(), i, 2);
            t.setValueAt(prod.getVenda(), i, 3);
            t.setValueAt(prod.getTotalitem(), i, 4);
            t.setValueAt(prod.getCodproduto(), i, 5);
        }

    }

    public void populaospesq() {

        DefaultTableModel t = (DefaultTableModel) tbos.getModel();
        t.setNumRows(0);
        String status = cbstatus.getSelectedItem().toString();

        if (cbstatus.getSelectedItem().equals("Todos")) {
            status = "";
        }
        if (tfcod.getText().equals("") || tfcodos.getText().equals("")) {
            if (tfcod.getText().equals("")) {
                tfcod.setText("0");
            }
            if (tfcodos.getText().equals("")) {
                tfcodos.setText("0");
            }

        }

        int tamanho;
        List<Os> lista = oscontrol.listaospesq(tfdescricao.getText(),
                Integer.parseInt(tfcod.getText()), status,
                Util.datatoStringUS(tfdtai.getDate()),
                Util.datatoStringUS(tfdtaf.getDate()),
                Util.datatoStringUS(tfdtfi.getDate()),
                Util.datatoStringUS(tfdtff.getDate()),
                Integer.parseInt(tfcodos.getText()));

        if (lista.isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "Nenhuma O.S encontrado com o filtro selecionado");
        }

        tamanho = lista.size();
        for (int i = 0; i < tamanho; i++) {
            Os os = lista.get(i);

            t.addRow(new Object[]{0});

            t.setValueAt(os.getCod(), i, 0);
            t.setValueAt(os.getDescricao(), i, 1);
            t.setValueAt(Util.convertedataBR(os.getDta()), i, 2);
            if (os.getDtf() == null) {
                t.setValueAt("", i, 3);
            } else {
                t.setValueAt(Util.convertedataBR(os.getDtf()), i, 3);
            }
            t.setValueAt(os.getStatus(), i, 4);
            t.setValueAt(os.getCodclifor(), i, 5);
            t.setValueAt(os.getClifor(), i, 6);
            t.setValueAt(os.getUseros(), i, 7);
            t.setValueAt(os.getObs(), i, 8);
            t.setValueAt(os.getTotal(), i, 9);
            t.setValueAt(os.getDesconto(), i, 10);
            t.setValueAt(os.getSubtotal(), i, 11);
            if (os.getDtg() == null) {
                t.setValueAt("", i, 12);
            } else {
                t.setValueAt(Util.convertedataBR(os.getDtg()), i, 12);
            }

        }
    }

