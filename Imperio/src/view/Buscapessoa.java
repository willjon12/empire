    private void btpesqpessoaokActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btpesqpessoaokActionPerformed
        int posicao = tblPessoa.getSelectedRow();
        if (posicao >= 0) {
            if (aba1 == 1) {

                u.setCodusuario(Integer.parseInt(tblPessoa.getValueAt(posicao, 0).toString()));
                u.setNome(tblPessoa.getValueAt(posicao, 1).toString());
                u.setSenha(tblPessoa.getValueAt(posicao, 2).toString());
                u.setPessoas(Boolean.parseBoolean(tblPessoa.getValueAt(posicao, 3).toString()));
                u.setUsuarios(Boolean.parseBoolean(tblPessoa.getValueAt(posicao, 4).toString()));
                u.setProdutos(Boolean.parseBoolean(tblPessoa.getValueAt(posicao, 5).toString()));
                u.setApagar(Boolean.parseBoolean(tblPessoa.getValueAt(posicao, 6).toString()));
                u.setAreceber(Boolean.parseBoolean(tblPessoa.getValueAt(posicao, 7).toString()));
                u.setOs(Boolean.parseBoolean(tblPessoa.getValueAt(posicao, 8).toString()));
                this.dispose();

            } else {
                p.setCodpessoa(Integer.parseInt(tblPessoa.getValueAt(posicao, 0).toString()));
                p.setNome(tblPessoa.getValueAt(posicao, 1).toString());
                p.setApelido(tblPessoa.getValueAt(posicao, 2).toString());
                p.setCPF(tblPessoa.getValueAt(posicao, 3).toString());
                String pessoa = tblPessoa.getValueAt(posicao, 4).toString();
                if (pessoa.equals("Física")) {
                    p.setPfisica(true);
                } else {
                    p.setPfisica(false);
                }
                p.setCelular(tblPessoa.getValueAt(posicao, 5).toString());
                p.setTelefone(tblPessoa.getValueAt(posicao, 6).toString());
                p.setEndereco(tblPessoa.getValueAt(posicao, 7).toString());
                p.setEnumero(tblPessoa.getValueAt(posicao, 8).toString());
                p.setBairro(tblPessoa.getValueAt(posicao, 9).toString());
                p.setComplemento(tblPessoa.getValueAt(posicao, 10).toString());
                p.setCEP(tblPessoa.getValueAt(posicao, 11).toString());
                p.setCidade(tblPessoa.getValueAt(posicao, 12).toString());
                p.setUF(tblPessoa.getValueAt(posicao, 13).toString());
                p.setNascimento(tblPessoa.getValueAt(posicao, 14).toString());
                p.setDatacadastro(tblPessoa.getValueAt(posicao, 15).toString());
                p.setRG(tblPessoa.getValueAt(posicao, 16).toString());
                p.setEmissorRG(tblPessoa.getValueAt(posicao, 17).toString());
                p.setSexo(tblPessoa.getValueAt(posicao, 18).toString());
                p.setEmail(tblPessoa.getValueAt(posicao, 19).toString());
                p.setOBS(tblPessoa.getValueAt(posicao, 20).toString());

                this.dispose();
            }

        } else {
            JOptionPane.showMessageDialog(rootPane, "Selecione um Registro");
        }


    }//GEN-LAST:event_btpesqpessoaokActionPerformed

