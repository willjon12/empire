    private void ut() {

        DefaultTableModel t = (DefaultTableModel) tblItens.getModel();
        int ul = t.getRowCount();
        double subtotal = 0;
        double desconto = Double.parseDouble(tfdesconto.getText().replace(",", "."));

        for (int l = 0; l < ul; l++) {

            subtotal += Double.parseDouble(t.getValueAt(l, 4).toString());

        }
        double descfinal = desconto * subtotal / 100;

        tfsubtotal.setText(String.valueOf(dc.format(subtotal)));
        tftotal.setText(String.valueOf(dc.format(subtotal - descfinal)));
        tftotalos.setText(String.valueOf(dc.format(subtotal - descfinal)));

    }


