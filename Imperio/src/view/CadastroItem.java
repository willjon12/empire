    private void btexcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btexcluirActionPerformed
        String produto = tfdescricao.getText();
        int reply = JOptionPane.showConfirmDialog(null, "Deseja realmente Excluir o Item ", "Tem certeza", JOptionPane.YES_NO_OPTION);
        if (reply == JOptionPane.YES_OPTION) {

            prodcontrol.excluir(Integer.parseInt(tfcod.getText()));
            desativatela();
            limpatela();
            JOptionPane.showMessageDialog(rootPane, "Item " + produto + " Excluido com sucesso");
        } else {

        }
    }//GEN-LAST:event_btexcluirActionPerformed

    private void tfmargemFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_tfmargemFocusLost
        if (!tfcusto.getText().equals("") && !tfmargem.getText().equals("")) {
            custo = (Double.parseDouble(tfcusto.getText().replace(",", ".")));
            margem = (Double.parseDouble(tfmargem.getText().replace(",", "."))) / 100;

            venda = custo + custo * margem;

            tfvenda.setText(dc.format(venda));
        }
    }//GEN-LAST:event_tfmargemFocusLost

