    private void btokActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btokActionPerformed
        int posicao = tbos.getSelectedRow();
        if (posicao >= 0) {

            o.setCod(Integer.parseInt(tbos.getValueAt(posicao, 0).toString()));
            o.setDescricao(tbos.getValueAt(posicao, 1).toString());
            o.setDta(tbos.getValueAt(posicao, 2).toString());
            o.setDtf(tbos.getValueAt(posicao, 3).toString());
            o.setStatus(tbos.getValueAt(posicao, 4).toString());
            o.setCodclifor(Integer.parseInt(tbos.getValueAt(posicao, 5).toString()));
            o.setClifor(tbos.getValueAt(posicao, 6).toString());
            o.setUseros(tbos.getValueAt(posicao, 7).toString());
            o.setObs(tbos.getValueAt(posicao, 8).toString());
            o.setTotal(Double.parseDouble(tbos.getValueAt(posicao, 9).toString()));
            o.setDesconto(Double.parseDouble(tbos.getValueAt(posicao, 10).toString()));
            o.setSubtotal(Double.parseDouble(tbos.getValueAt(posicao, 11).toString()));
            o.setDtg(tbos.getValueAt(posicao, 12).toString());

            itens = new ArrayList<>();
            int l = tbios.getRowCount();
            for (int i = 0; i < l; i++) {
                Item it = new Item();
                it.setDescricao(tbios.getValueAt(i, 0).toString());
                it.setUN(tbios.getValueAt(i, 1).toString());
                it.setQtd(Double.parseDouble(tbios.getValueAt(i, 2).toString()));
                it.setVenda(Double.parseDouble(tbios.getValueAt(i, 3).toString()));
                it.setTotalitem(Double.parseDouble(tbios.getValueAt(i, 4).toString()));
                DefaultTableModel t = (DefaultTableModel) tbios.getModel();
                it.setId(Integer.parseInt(t.getValueAt(i, 5).toString()));
                itens.add(it);

            }

            this.dispose();
        } else {
            JOptionPane.showMessageDialog(rootPane, "Selecione um Registro");
        }

    }//GEN-LAST:event_btokActionPerformed

