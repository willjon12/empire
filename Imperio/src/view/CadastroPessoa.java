    private void btsalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btsalvarActionPerformed
        aba = tabcad.getSelectedIndex();
        if (aba == 0) {

            String nomecampos = "";
            if (tfnomerazao.getText().equals("") | tfcpfcnpj.getText().equals("   .   .   -  ")
                    | tfcpfcnpj.getText().equals("  .   .   /    -  ") | tfcelular.getText().equals("(0xx  )     -    ") | !cpfcnpj) {
                if (tfnomerazao.getText().equals("")) {

                    nomecampos += " Nome ";
                    tfnomerazao.requestFocus();
                }
                if (tfcpfcnpj.getText().equals("   .   .   -  ") | tfcpfcnpj.getText().equals("  .   .   /    -  ")) {

                    nomecampos += ", CPF/CNPJ ";
                }
                if (tfcelular.getText().equals("(0xx  )     -    ")) {

                    nomecampos += ", Celular ";

                }
                JOptionPane.showMessageDialog(cbos, "Preencha o(s) campo(s) obrigatórios (" + nomecampos + ")");
            } else {
                if (flag == 1) {

                    if (pcontrol.salvar(rbf.isSelected(), tfnomerazao.getText(), Util.limpaCPF(tfcpfcnpj.getText()), tfcelular.getText(), Util.convertedataUS(tfnascimento.getText()), tfendereco.getText(),
                            tfapefan.getText(), tfsexo.getText(), tfenumero.getText(), tfbairro.getText(), tfcomplemento.getText(), tfCEP.getText(),
                            tfcidade.getText(), tfemail.getText(), tfUF.getText(), Util.convertedataUS(tfdatacad.getText()), tfrgie.getText(), tfemissorrg.getText(),
                            tftelefone.getText(), tfOBS.getText())) {
                        JOptionPane.showMessageDialog(cbos, "Cadastro efetuado com sucesso");
                        limpatelacf();
                        btnovo.setEnabled(true);
                        btsalvar.setEnabled(false);
                        btexcluir.setEnabled(false);
                        tabcad.setEnabledAt(1, true);
                        desativacf();

                    } else {
                        JOptionPane.showMessageDialog(cbos, "Cadastro não efetuado");
                    }

                }
                if (flag == 2) {
                    if (pcontrol.alterar(Integer.parseInt(tfcod.getText()), rbf.isSelected(), tfnomerazao.getText(), Util.limpaCPF(tfcpfcnpj.getText()), tfcelular.getText(), Util.convertedataUS(tfnascimento.getText()), tfendereco.getText(),
                            tfapefan.getText(), tfsexo.getText(), tfenumero.getText(), tfbairro.getText(), tfcomplemento.getText(), tfCEP.getText(),
                            tfcidade.getText(), tfemail.getText(), tfUF.getText(), Util.convertedataUS(tfdatacad.getText()), tfrgie.getText(), tfemissorrg.getText(),
                            tftelefone.getText(), tfOBS.getText())) {
                        JOptionPane.showMessageDialog(cbos, "Cadastro '" + tfnomerazao.getText() + "' alterado com sucesso");
                        limpatelacf();
                        btnovo.setEnabled(true);
                        btsalvar.setEnabled(false);
                        btexcluir.setEnabled(false);
                        tabcad.setEnabledAt(1, true);
                        desativacf();

                    } else {
                        JOptionPane.showMessageDialog(cbos, "Alteração não efetuado");
                    }

                }

            }
        }
        if (aba == 1) {

            if (tfusuario.getText().equals("") | tfsenha.getText().equals("")) {
                JOptionPane.showMessageDialog(cbos, "Preencha os campos obrigatórios");
            } else {
                if (flag == 1) {
                    if (String.valueOf(tfsenha.getPassword()).equals(String.valueOf(tfconfirmasenha.getPassword()))) {

                        if (ucontrol.salvar(tfusuario.getText(), tfsenha.getText(), cbpessoa.isSelected(),
                                cbusuario.isSelected(), cbproduto.isSelected(), cbpagar.isSelected(), cbreceber.isSelected(), cbos.isSelected())) {
                            JOptionPane.showMessageDialog(cbos, "Cadastro efetuado com sucesso");
                            limpatelau();
                            btnovo.setEnabled(true);
                            btsalvar.setEnabled(false);
                            btexcluir.setEnabled(false);
                            tabcad.setEnabledAt(0, true);
                            desativau();

                        } else {
                            JOptionPane.showMessageDialog(cbos, "Cadastro não efetuado");
                        }
                    } else {
                        JOptionPane.showMessageDialog(cbos, "A senha e a confirmação da senha não correspondem. Favor preencher corretamente");
                    }
                }
                if (flag == 2) {

                    if (String.valueOf(tfsenha.getPassword()).equals(String.valueOf(tfconfirmasenha.getPassword()))) {
                        u.setNome(tfusuario.getText());
                        u.setSenha(tfsenha.getText());

                        if (ucontrol.alterar(Integer.parseInt(tfcodu.getText()), tfusuario.getText(), tfsenha.getText(), cbpessoa.isSelected(),
                                cbusuario.isSelected(), cbproduto.isSelected(), cbpagar.isSelected(), cbreceber.isSelected(), cbos.isSelected())) {
                            JOptionPane.showMessageDialog(cbos, "Cadastro Alterado com sucesso");
                            limpatelau();
                            desativau();
                            btnovo.setEnabled(true);
                            btsalvar.setEnabled(false);
                            btexcluir.setEnabled(false);
                            tabcad.setEnabledAt(0, true);

                        } else {
                            JOptionPane.showMessageDialog(cbos, "Usuário já existente");
                        }
                    } else {
                        JOptionPane.showMessageDialog(cbos, "A senha e a confirmação da senha não correspondem. Favor preencher corretamente");
                    }

                }
            }
        }
    }//GEN-LAST:event_btsalvarActionPerformed


    private void btexcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btexcluirActionPerformed
        aba = tabcad.getSelectedIndex();

        if (aba == 1) {
            String udel;
            udel = tfusuario.getText();
            int reply = JOptionPane.showConfirmDialog(null, "Deseja realmente excluir o usuário " + udel + "?", "Tem certeza ?", JOptionPane.YES_NO_OPTION);
            if (reply == JOptionPane.YES_OPTION) {
                if (ucontrol.excluir(Integer.parseInt(tfcodu.getText())));
                {

                    JOptionPane.showMessageDialog(cbos, "Usuário " + udel + " excluido com sucesso");
                    limpatelau();
                    desativau();
                    btsalvar.setEnabled(false);
                    btnovo.setEnabled(true);
                    btexcluir.setEnabled(false);
                    tabcad.setEnabledAt(0, true);
                }

            } else {
                JOptionPane.showMessageDialog(cbos, "Nehuma alteração realizada");
            }

        }
        if (aba == 0) {
            String pdel;
            pdel = tfnomerazao.getText();
            int reply = JOptionPane.showConfirmDialog(null, "Deseja realmente excluir o Cadastro " + pdel + "?", "Tem certeza ?", JOptionPane.YES_NO_OPTION);
            if (reply == JOptionPane.YES_OPTION) {
                if (pcontrol.excluir(Integer.parseInt(tfcod.getText())));
                {

                    JOptionPane.showMessageDialog(cbos, "Cadastro " + pdel + " excluido com sucesso");
                    limpatelacf();
                    desativacf();
                    btsalvar.setEnabled(false);
                    btnovo.setEnabled(true);
                    btexcluir.setEnabled(false);
                    tabcad.setEnabledAt(1, true);
                }

            } else {
                JOptionPane.showMessageDialog(cbos, "Nehuma alteração realizada");
            }
        }


    }//GEN-LAST:event_btexcluirActionPerformed
