    private void btpesqActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btpesqActionPerformed

        buscaprod = new Buscaitem(null, true, tfdescricao.getText());
        buscaprod.setVisible(true);
        if (buscaprod.p.getCodproduto() > 0) {
            tfcod.setText(String.valueOf(buscaprod.p.getCodproduto()));
            tfdescricao.setText(buscaprod.p.getDescricao());
            tfcusto.setText(String.valueOf(buscaprod.p.getCusto()));
            tfvenda.setText(String.valueOf(buscaprod.p.getVenda()));
            tfmargem.setText(String.valueOf(buscaprod.p.getLucro()));
            tfun.setText(String.valueOf(buscaprod.p.getUN()));
            atualizacampos();
        }


    }//GEN-LAST:event_btpesqActionPerformed

    private void tfqtdFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_tfqtdFocusLost
        if (!tfqtd.getText().equals("")) {
            if (Double.parseDouble(tfqtd.getText().replace(",", ".")) == 0) {
                tfqtd.setText("1");
            }
        }

        if (!tfvenda.getText().equals("") && !tfqtd.getText().equals("")) {
            qtd = Double.parseDouble(tfqtd.getText().replace(",", "."));
            venda = Double.parseDouble(tfvenda.getText().replace(",", "."));

            tftotitem.setText(dc.format(venda * qtd));
        }
    }//GEN-LAST:event_tfqtdFocusLost

    private void tfmargemFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_tfmargemFocusLost

        if (!tfcusto.getText().equals("") && !tfmargem.getText().equals("")) {
            custo = (Double.parseDouble(tfcusto.getText().replace(",", ".")));
            margem = (Double.parseDouble(tfmargem.getText().replace(",", "."))) / 100;

            venda = custo + custo * margem;
            tfvenda.setText(dc.format(venda));
        }
        if (!tfvenda.getText().equals("") && !tfqtd.getText().equals("")) {
            qtd = Double.parseDouble(tfqtd.getText().replace(",", "."));
            tftotitem.setText(String.valueOf(dc.format(Double.parseDouble(tfvenda.getText().replace(",", ".")) * qtd)));
        }
        if (tfmargem.getText().isEmpty()) {
            tfmargem.setText("45,00");
        }
    }//GEN-LAST:event_tfmargemFocusLost

    private void btokActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btokActionPerformed

        if (tfdescricao.getText().equals("") || tfqtd.getText().equals("") || tfun.getText().equals("")
                || tfvenda.getText().equals("") ) {
            JOptionPane.showMessageDialog(rootPane, "Escolha um item em 'pesquisar' ou preencha os campos obrigatórios!");
        } else {
            i.setDescricao(tfdescricao.getText());
            i.setQtd(Double.parseDouble(tfqtd.getText().replace(",", ".")));
            i.setUN(tfun.getText());
            i.setVenda(Double.parseDouble(tfvenda.getText().replace(",", ".")));
//            i.setCusto(Double.parseDouble(tfcusto.getText().replace(",", ".")));
            i.setTotalitem(Double.parseDouble(tftotitem.getText().replace(",", ".")));
//            i.setLucro(Double.parseDouble(tftotitem.getText().replace(",", ".")));
            i.setFechado(false);
            this.dispose();

        }
    }//GEN-LAST:event_btokActionPerformed

    private void tfvendaFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_tfvendaFocusLost
        if (!tfcusto.getText().equals("") && !tfvenda.getText().equals("")) {
            qtd = Double.parseDouble(tfqtd.getText().replace(",", "."));
            venda = Double.parseDouble(tfvenda.getText().replace(",", "."));
            custo = Double.parseDouble(tfcusto.getText().replace(",", "."));
            sub = venda - custo;
            marg = sub / custo * 100;
            tfmargem.setText(String.valueOf(dc.format(marg)));

        }
        if (!tfqtd.getText().equals("") && !tfvenda.getText().equals("")) {
            qtd = Double.parseDouble(tfqtd.getText().replace(",", "."));
            venda = Double.parseDouble(tfvenda.getText().replace(",", "."));

            tftotitem.setText(dc.format(venda * qtd));
        }

    }//GEN-LAST:event_tfvendaFocusLost

