    private void btpesqActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btpesqActionPerformed
        limpatela();
        desativatela();

        buscaprod = new Buscaitem(this, true, tfpesq.getText());
        buscaprod.setVisible(true);
        if (buscaprod.p.getCodproduto() >= 1) {
            tfcod.setText(String.valueOf(buscaprod.p.getCodproduto()));
            tfdescricao.setText(buscaprod.p.getDescricao());
            tfdatacad.setText(buscaprod.p.getDatacad());
            tfcusto.setText(String.valueOf(dc.format(buscaprod.p.getCusto())));
            tfvenda.setText(String.valueOf(dc.format(buscaprod.p.getVenda())));
            tfmargem.setText(String.valueOf(dc.format(buscaprod.p.getLucro())));
            tfun.setText(String.valueOf(buscaprod.p.getUN()));
            btnovo.setEnabled(false);
            btsalvar.setEnabled(true);
            btexcluir.setEnabled(true);
            ativatela();
            flag = 2;

        } else {
            btnovo.setEnabled(true);
        }

    }//GEN-LAST:event_btpesqActionPerformed

