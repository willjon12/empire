    private int calcdias() {
        try {
            int dias = Days.daysBetween(Util.DateToDTM(tfdtf.getDate()),
                    Util.DateToDTM(tfdtg.getDate())).getDays();

            return dias;
        } catch (Exception ex) {
            printStackTrace(ex);
            return 0;

        }

    }

    private void salvaros() {
        Os o = new Os();

        o.setDta(Util.datatoStringUS(tfdta.getDate()));
        o.setDtf(Util.datatoStringUS(tfdtf.getDate()));
        o.setDtg(Util.datatoStringUS(tfdtg.getDate()));
        o.setUseros(tfusuario.getText());
        o.setDescricao(tfdescricao.getText());
        o.setStatus(cbstatus.getSelectedItem().toString());
        o.setCodclifor(Integer.parseInt(tfcod.getText()));
        o.setClifor(tfcliente.getText());
        o.setObs(tfobs.getText());
        o.setSubtotal(Double.parseDouble(tfsubtotal.getText().replace(",", ".")));
        o.setDesconto(Double.parseDouble(tfdesconto.getText().replace(",", ".")));
        o.setTotal(Double.parseDouble(tftotal.getText().replace(",", ".")));

        Item i = new Item();
        OsDAO osdao = new OsDAO();

        if (flag == 1) {
            osg = oscontrol.salvar(o);
            i = osdao.lastid();

        }
        if (flag == 2) {
            o.setCod(Integer.parseInt(tfcodos.getText()));
            osg = oscontrol.alterar(o);
            if (tos.titos.size() > 0) {

                for (int tt = 0; tt < tos.titos.size(); tt++) {

                    ostg = tc.salvar(tos.titos.get(tt));

                }

            }

        }
        DefaultTableModel t = (DefaultTableModel) tblItens.getModel();
        int ul = t.getRowCount();
        int codos = i.getCodos();
        for (int l = 0; l < ul; l++) {

            i.setCodos(codos);
            i.setDescricao(t.getValueAt(l, 0).toString());
            i.setQtd(Double.parseDouble(t.getValueAt(l, 1).toString()));
            i.setUN(t.getValueAt(l, 2).toString());
            i.setVenda(Double.parseDouble(t.getValueAt(l, 3).toString()));
            i.setTotalitem(Double.parseDouble(t.getValueAt(l, 4).toString()));
            if (flag == 2) {
                i.setId(Integer.parseInt(t.getValueAt(l, 5).toString()));
            }
            if (flag == 1) {
                osig = oscontrol.salvaritensos(i);
            }
            if (flag == 2 && i.getId() != 0) {
                osig = oscontrol.alteraritensos(i);
            }
            if (i.getId() == 0 && flag == 2) {
                i.setCodos(Integer.parseInt(tfcodos.getText()));
                osig = oscontrol.salvaritensos(i);
            }
        }
        if (flag == 2 && exios.size() > 0) {
            try {
                int ule = exios.size();
                for (int l = 0; l < ule; l++) {
                    Item ix = exios.get(l);
                    oscontrol.removeritensos(ix.getCodproduto());
                }
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(rootPane, "Erro ao excluir itens removidos da O.S");
            }

        }
        if (flag == 1 | flag == 2) {
            if (osg && osig) {
                JOptionPane.showMessageDialog(rootPane, "Dados Gravados com sucesso");
                limpaos();
                desativaOS();
                flag = 0;
            }
        }

        if (flag == 2 && tos.titos.size() > 0) {
            if (osg && osig && ostg) {
                JOptionPane.showMessageDialog(rootPane, "Dados Gravados com sucesso. A O.S foi Fechada ");
                limpaos();
                desativaOS();
                flag = 0;
            }
        }

    }

