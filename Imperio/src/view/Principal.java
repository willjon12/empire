    private void menuMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_menuMouseClicked
        // TODO add your handling code here:

        if (evt.getClickCount() == 2) {
            TreeNode node = (TreeNode) menu.getSelectionPath().getLastPathComponent();
            //testa se é o ultimo nó
            if (node.isLeaf()) {
                if (node.toString().contains("Pessoa")) {
                    if (Util.usuario.isPessoas()) {
                        new CadastroPessoa().setVisible(true);
                    } else {
                        JOptionPane.showMessageDialog(rootPane, "Usuário " + Util.usuario.getNome()
                                + " não possui permissão para acessar este módulo");
                    }

                } else if (node.toString().contains("Itens")) {
                    if (Util.usuario.isProdutos()) {
                        new CadastroItem().setVisible(true);
                    } else {
                        JOptionPane.showMessageDialog(rootPane, "Usuário " + Util.usuario.getNome()
                                + " não possui permissão para acessar este módulo");
                    }

                } else if (node.toString().contains("A Re")) {
                    if (Util.usuario.isAreceber() | Util.usuario.isApagar()) {
                        new Titulos().setVisible(true);
                    } else {
                        JOptionPane.showMessageDialog(rootPane, "Usuário " + Util.usuario.getNome()
                                + " não possui permissão para acessar este módulo");
                    }

                } else if (node.toString().contains("O.S")) {
                    if (Util.usuario.isOs()) {
                        new OS().setVisible(true);
                    } else {
                        JOptionPane.showMessageDialog(rootPane, "Usuário " + Util.usuario.getNome()
                                + " não possui permissão para acessar este módulo");
                    }

                }
            }

    }//GEN-LAST:event_menuMouseClicked
    }
