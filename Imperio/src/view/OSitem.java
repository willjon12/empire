    private void btsalvaritemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btsalvaritemActionPerformed
        if (tfdescricao.getText().equals("") || tfqtd.getText().equals("") || tfun.getText().equals("")
                || tfvenda.getText().equals("") || tfmargem.getText().isEmpty() || tfcusto.getText().isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "Escolha um item em 'pesquisar' ou preencha os campos obrigatórios!");
        } else {
            int reply = JOptionPane.showConfirmDialog(null, "Deseja Cadastrar o Item ", "Cadastrar?", JOptionPane.YES_NO_OPTION);
            if (reply == JOptionPane.YES_OPTION) {
                i.setDescricao(tfdescricao.getText());
                i.setQtd(Double.parseDouble(tfqtd.getText().replace(",", ".")));
                i.setUN(tfun.getText());
                i.setVenda(Double.parseDouble(tfvenda.getText().replace(",", ".")));
                i.setCusto(Double.parseDouble(tfcusto.getText().replace(",", ".")));
                i.setTotalitem(Double.parseDouble(tftotitem.getText().replace(",", ".")));
                i.setLucro(Double.parseDouble(tftotitem.getText().replace(",", ".")));
                i.setFechado(false);
                Date data = new Date();
                SimpleDateFormat databr = new SimpleDateFormat("yyyy/MM/dd");
                i.setDatacad(databr.format(data));
                if (icontrol.salvar2(i)) {
                    JOptionPane.showMessageDialog(rootPane, "Item cadastrado com Sucesso");
                } else {
                    JOptionPane.showMessageDialog(rootPane, "Erro ao cadastrar item");
                }
                this.dispose();
            } else {

            }
        }
    }//GEN-LAST:event_btsalvaritemActionPerformed

    private void atualizacampos() {
        qtd = Double.parseDouble(tfqtd.getText().replace(",", "."));
        venda = Double.parseDouble(tfvenda.getText().replace(",", "."));
        custo = Double.parseDouble(tfcusto.getText().replace(",", "."));
        margem = Double.parseDouble(tfmargem.getText().replace(",", ".")) / 100;
        tftotitem.setText(dc.format(venda * qtd));

    }

