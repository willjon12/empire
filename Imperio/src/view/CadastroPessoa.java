    private void tfcpfcnpjFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_tfcpfcnpjFocusLost
        if (tfcpfcnpj.getText().equals("   .   .   -  ") | tfcpfcnpj.getText().equals("  .   .   /    -  ")) {
            lbcpfcnpj.setText("Preencha o CPF/CNPJ");
            tfcpfcnpj.requestFocus();
    }//GEN-LAST:event_tfcpfcnpjFocusLost
        
    else {
            if (rbf.isSelected()) {
                if (Cnp.isValidCPF(tfcpfcnpj.getText().replace(".", "").replace("-", "").replace(" ", ""))) {
                    lbcpfcnpj.setText("");
                    cpfcnpj = true;
                } else {
                    lbcpfcnpj.setText("CPF Inválido");
                    tfcpfcnpj.requestFocus();
                    cpfcnpj = false;
                }

            }
            if (rbj.isSelected()) {
                if (Cnp.isValidCNPJ(tfcpfcnpj.getText().replace(".", "").replace("-", "").replace("/", "").replace(" ", ""))) {
                    lbcpfcnpj.setText("");
                    cpfcnpj = true;
                } else {
                    lbcpfcnpj.setText("CNPJ Inválido");
                    tfcpfcnpj.requestFocus();
                    cpfcnpj = false;
                }
            }

        }
    }

